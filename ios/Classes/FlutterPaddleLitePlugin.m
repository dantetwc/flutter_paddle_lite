      methodChannelWithName:@"flutter_paddle_lite"
            binaryMessenger:[registrar messenger]];
  FlutterPaddleLitePlugin* instance = [[FlutterPaddleLitePlugin alloc] init];
  [registrar addMethodCallDelegate:instance channel:channel];
}

